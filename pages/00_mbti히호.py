def main():
    st.set_page_config(page_title="MBTI 진로 추천 💡", page_icon="🧭")
    st.title("MBTI로 찾는 진로 추천")
    st.write("간단하게 MBTI를 골라봐. 그 유형에 잘 맞는 진로 2가지를 추천해줄게. 😊")

    mbti_list = list(MBTI_CAREERS.keys())
    mbti = st.selectbox("MBTI 선택", mbti_list)

    if st.button("추천 보기 👀"):
        careers = MBTI_CAREERS.get(mbti)
        if not careers:
            st.write("죄송해요, 해당 MBTI에 대한 정보가 없어요.")
            return

        st.subheader(f"{mbti} 유형의 추천 진로")
        for idx, item in enumerate(careers, start=1):
            st.markdown(f"### {idx}. {item['career']}")
            st.write(f"**어떤 학과가 적합한가요?** — {', '.join(item['majors'])}")
            st.write(f"**어떤 사람이 잘 맞을까?** — {item['fit']}")
            st.write("---")

        st.info("팁: 진로는 성격뿐 아니라 경험, 관심사, 환경에 따라 달라져요. 여러 옵션을 시도해보고 마음에 드는 길을 찾아봐! ✨")

    st.write("\n---\n")
    st.caption("이 앱은 학습용으로 만든 간단한 추천 도구예요. 최종 선택은 자기 탐색과 실제 경험을 통해 결정하세요.")


if __name__ == '__main__':
    main()
