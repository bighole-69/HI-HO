import streamlit as st
import folium
from streamlit_folium import st_folium

st.set_page_config(page_title="서울 관광지도 🌏", layout="wide")

st.title("🌆 외국인이 좋아하는 서울 관광지 Top 10 지도")
st.write("아래 지도에서 관광지를 클릭하면 이름과 간단한 설명을 볼 수 있어요!")

# 관광지 데이터 (이름, 위도, 경도, 설명)
places = [
    ("경복궁 🏯", 37.579617, 126.977041, "대한민국 대표 궁궐, 전통 문화의 중심지."),
    ("명동 쇼핑거리 🛍️", 37.563757, 126.982684, "외국인들이 가장 많이 찾는 쇼핑 명소."),
    ("남산타워 (N서울타워) 🗼", 37.551169, 126.988227, "서울을 한눈에 볼 수 있는 전망 명소."),
    ("홍대 거리 🎶", 37.557192, 126.923895, "젊음과 예술의 중심, 스트리트 공연과 카페."),
    ("북촌 한옥마을 🏘️", 37.582604, 126.983998, "전통 한옥이 모여있는 고즈넉한 마을."),
    ("동대문 디자인 플라자(DDP) 🏙️", 37.566295, 127.009308, "미래적인 외관의 디자인·전시 공간."),
    ("광장시장 🍜", 37.570030, 127.000178, "빈대떡, 마약김밥으로 유명한 전통 시장."),
    ("롯데월드타워 전망대 🏙️", 37.512558, 127.102623, "대한민국에서 가장 높은 타워."),
    ("청계천 🌿", 37.568941, 126.977220, "도심 속 휴식 공간, 산책 명소."),
    ("코엑스 아쿠아리움 🐠", 37.511307, 127.059019, "가족과 연인에게 인기 많은 수족관."),
]

# 지도 기본 위치: 서울 중심
m = folium.Map(location=[37.5665, 126.9780], zoom_start=12)

# 지도에 마커 추가
for name, lat, lon, desc in places:
    folium.Marker(
        location=[lat, lon],
        popup=f"<b>{name}</b><br>{desc}",
        tooltip=name
    ).add_to(m)

st_folium(m, width=800, height=600)
